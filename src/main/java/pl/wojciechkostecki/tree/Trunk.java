package pl.wojciechkostecki.tree;

public class Trunk {
    private double widthTrunk;

    private double heightTrunk;

    public double getWidthTrunk() {
        return widthTrunk;
    }

    public void setWidthTrunk(double widthTrunk) {
        this.widthTrunk = widthTrunk;
    }

    public double getHeightTrunk() {
        return heightTrunk;
    }

    public void setHeightTrunk(double heightTrunk) {
        this.heightTrunk = heightTrunk;
    }
}
